function resolveLocaleCode(value: string): string {
	for (let i = 0; i < languageOptions.length; i++) {
		const option = languageOptions[i]
		if (option.code == value) {
			return option.code
		}
	}
	return DEFAULT_LOCALE
}

function getLocaleBundle(locale: string): UTSJSONObject {
	switch (locale) {
		case 'zh-Hans':
			return zhHansMessages
		case 'zh-Hant':
			return zhHantMessages
		case 'ms':
			return msMessages
		case 'vi':
			return viMessages
		case 'th':
			return thMessages
		default:
			return enMessages
	}
}

function readStoredLocale(): string {
	const value = uni.getStorageSync(LOCALE_STORAGE_KEY)
	if (typeof value == 'string') {
		return resolveLocaleCode(value as string)
	}
	return DEFAULT_LOCALE
}

export const i18nStore = reactive<I18nState>({
	locale: DEFAULT_LOCALE,
	version: 0,
})

export function initializeLocale(): string {
	const locale = readStoredLocale()
	i18nStore.locale = locale
	i18nStore.version = i18nStore.version + 1
	return locale
}

export function getCurrentLocale(): string {
	return i18nStore.locale
}

export function setLocale(locale: string): string {
	const nextLocale = resolveLocaleCode(locale)
	const hasChanged = i18nStore.locale != nextLocale
	i18nStore.locale = nextLocale
	uni.setStorageSync(LOCALE_STORAGE_KEY, nextLocale)
	if (hasChanged) {
		i18nStore.version = i18nStore.version + 1
	}
	applyTabBarLocale()
	return nextLocale
}

export function t(key: string): string {
	const currentBundle = getLocaleBundle(i18nStore.locale)
	const currentValue = currentBundle[key]
	if (typeof currentValue == 'string') {
		return currentValue as string
	}
	const fallbackValue = enMessages[key]
	if (typeof fallbackValue == 'string') {
		return fallbackValue as string
	}
	return key
}

export function getLanguageOptions(): LanguageOption[] {
	return languageOptions
}

export function getCurrentLanguageLabel(): string {
	const locale = getCurrentLocale()
	for (let i = 0; i < languageOptions.length; i++) {
		const option = languageOptions[i]
		if (option.code == locale) {
			return option.nativeName
		}
	}
	return languageOptions[0].nativeName
}

