export function translateMemberType(memberType: string): string {
	if (memberType == '普通用户' || memberType == 'member' || memberType == 'Member') {
		return t('member.normal')
	}
	if (memberType == '客服' || memberType == 'support' || memberType == 'Support') {
		return t('member.support')
	}
	if (memberType == '主播' || memberType == 'anchor' || memberType == 'Host') {
		return t('member.anchor')
	}
	return memberType
}

